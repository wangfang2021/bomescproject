namespace SPPSApi.Controllers.G12
{
    [Route("api/FS1214/[action]")]
    [EnableCors("any")]
    [ApiController]
    public class FS1214Controller : BaseController
    {
        private readonly IWebHostEnvironment _webHostEnvironment;

        FS1214_Logic logic = new FS1214_Logic();
        private readonly string FunctionID = "FS1214";

        public FS1214Controller(IWebHostEnvironment webHostEnvironment)
        {
            _webHostEnvironment = webHostEnvironment;
        }

        #region 页面初始化
        [HttpPost]
        [EnableCors("any")]
        public string pageloadApi()
        {
            string strToken = Request.Headers["X-Token"];
            if (!isLogin(strToken))
            {
                return error_login();
            }
            LoginInfo loginInfo = getLoginByToken(strToken);
            //以下开始业务处理
            ApiResult apiResult = new ApiResult();
            try
            {
                Dictionary<string, Object> res = new Dictionary<string, Object>();
                List<Object> dataList_DataNameSource = ComFunction.convertAllToResult(logic.GetDataName());
                res.Add("DataNameSource", dataList_DataNameSource);
                apiResult.code = ComConstant.SUCCESS_CODE;
                apiResult.data = res;
                return JsonConvert.SerializeObject(apiResult, Formatting.Indented, JSON_SETTING);
            }
            catch (Exception ex)
            {
                ComMessage.GetInstance().ProcessMessage(FunctionID, "M00UE0006", ex, loginInfo.UserId);
                apiResult.code = ComConstant.ERROR_CODE;
                apiResult.data = "初始化失败";
                return JsonConvert.SerializeObject(apiResult, Formatting.Indented, JSON_SETTING);
            }
        }
        #endregion

        #region 检索
        [HttpPost]
        [EnableCors("any")]
        public string searchApi([FromBody] dynamic data)
        {
            string strToken = Request.Headers["X-Token"];
            if (!isLogin(strToken))
            {
                return error_login();
            }
            LoginInfo loginInfo = getLoginByToken(strToken);
            //以下开始业务处理
            ApiResult apiResult = new ApiResult();
            dynamic dataForm = JsonConvert.DeserializeObject(Convert.ToString(data));

            string vcConstCode = dataForm.vcConstCode;
            string vcData1 = dataForm.vcData1;
            string vcData2 = dataForm.vcData2;
            string vcData3 = dataForm.vcData3;

            vcConstCode = vcConstCode == null ? "" : vcConstCode;
            vcData1 = vcData1 == null ? "" : vcData1;
            vcData2 = vcData2 == null ? "" : vcData2;
            vcData3 = vcData3 == null ? "" : vcData3;
            try
            {
                DataTable dt = logic.GetSearchAll(vcConstCode, vcData1, vcData2, vcData3);
                DtConverter dtConverter = new DtConverter();
                List<Object> dataList = ComFunction.convertAllToResultByConverter(dt, dtConverter);
                apiResult.code = ComConstant.SUCCESS_CODE;
                apiResult.data = dataList;
                return JsonConvert.SerializeObject(apiResult, Formatting.Indented, JSON_SETTING);
            }
            catch (Exception ex)
            {
                ComMessage.GetInstance().ProcessMessage(FunctionID, "M03UE0901", ex, loginInfo.UserId);
                apiResult.code = ComConstant.ERROR_CODE;
                apiResult.data = "检索失败";
                return JsonConvert.SerializeObject(apiResult, Formatting.Indented, JSON_SETTING);
            }
        }
        #endregion
    }
}
